
public class Paciente  {
    private String nombre;
    private String afliccion;
    
    public Paciente(String nombre, String afliccion) {
        this.nombre = nombre;
        this.afliccion = afliccion;
        
    }

    public String getNombre() {
        return nombre;
    }

    
    public String getAfliccion() {
        return afliccion;
    }

}
